package com.algo.leetcode.arraysandhashing;

import org.junit.Before;
import org.junit.Test;

import static org.junit.Assert.*;

/**
 * Test class for ValidSudoku.
 */
public class ValidSudokuTest {

  private ValidSudoku sudoku;

  @Before
  public void preSetup() {
    sudoku = new ValidSudoku();
  }

  @Test
  public void testValidSudokuTrue() {
    char[][] board = {{'5','3','.','.','7','.','.','.','.'}
            ,{'6','.','.','1','9','5','.','.','.'}
            ,{'.','9','8','.','.','.','.','6','.'}
            ,{'8','.','.','.','6','.','.','.','3'}
            ,{'4','.','.','8','.','3','.','.','1'}
            ,{'7','.','.','.','2','.','.','.','6'}
            ,{'.','6','.','.','.','.','2','8','.'}
            ,{'.','.','.','4','1','9','.','.','5'}
            ,{'.','.','.','.','8','.','.','7','9'}};

    assertTrue(sudoku.isValidSudoku(board));
  }

  @Test
  public void testValidSudokuFalse() {
    char[][] board = {{'8','3','.','.','7','.','.','.','.'}
            ,{'6','.','.','1','9','5','.','.','.'}
            ,{'.','9','8','.','.','.','.','6','.'}
            ,{'8','.','.','.','6','.','.','.','3'}
            ,{'4','.','.','8','.','3','.','.','1'}
            ,{'7','.','.','.','2','.','.','.','6'}
            ,{'.','6','.','.','.','.','2','8','.'}
            ,{'.','.','.','4','1','9','.','.','5'}
            ,{'.','.','.','.','8','.','.','7','9'}};
    assertFalse(sudoku.isValidSudoku(board));
  }

  @Test
  public void testValidSudokuFail() {
    char[][] board = {{'.','.','4','.','.','.','6','3','.'}
            ,{'.','.','.','.','.','.','.','.','.'}
            ,{'5','.','.','.','.','.','.','9','.'}
            ,{'.','.','.','5','6','.','.','.','.'}
            ,{'4','.','3','.','.','.','.','.','1'}
            ,{'.','.','.','7','.','.','.','.','.'}
            ,{'.','.','.','5','.','.','.','.','.'}
            ,{'.','.','.','.','.','.','.','.','.'}
            ,{'.','.','.','.','.','.','.','.','.'}};
    assertFalse(sudoku.isValidSudoku(board));
  }
}